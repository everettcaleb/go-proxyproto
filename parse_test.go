package proxyproto

import (
	"bytes"
	"net"
	"reflect"
	"testing"
)

func Test_parse(t *testing.T) {
	tests := []struct {
		name    string
		buf     []byte
		want    *Data
		wantErr error
	}{
		{
			name: "valid 4",
			buf:  []byte("PROXY TCP4 10.20.30.40 40.30.20.10 8000 9000\r\nTEST"),
			want: &Data{
				remainingData: []byte("TEST"),
				AddressFamily: AddressFamilyIPv4,
				Transport:     TransportStream,
				SourceAddr:    net.IPv4(10, 20, 30, 40),
				SourcePort:    8000,
				DestAddr:      net.IPv4(40, 30, 20, 10),
				DestPort:      9000,
			},
		},
		{
			name: "valid 6",
			buf:  []byte("PROXY TCP6 2607:f8b0:4008:80e::200e 2606:4700:4700::1111 8000 9000\r\nTEST"),
			want: &Data{
				remainingData: []byte("TEST"),
				AddressFamily: AddressFamilyIPv6,
				Transport:     TransportStream,
				SourceAddr:    []byte{0x26, 0x7, 0xf8, 0xb0, 0x40, 0x8, 0x8, 0xe, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x20, 0x0e},
				SourcePort:    8000,
				DestAddr:      []byte{0x26, 0x6, 0x47, 0x0, 0x47, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x11, 0x11},
				DestPort:      9000,
			},
		},
		{
			name: "valid tcp4 proxy",
			buf: []byte{
				// header
				0x0D, 0x0A, 0x0D, 0x0A, 0x00, 0x0D, 0x0A, 0x51, 0x55, 0x49, 0x54, 0x0A,
				// version/command
				0x21,
				// address family / transport
				0x11,
				// length
				0x0, 0xc,
				// source addr
				10, 20, 30, 40,
				// dest addr
				40, 30, 20, 10,
				// source port
				0x1f, 0x40,
				// dest port
				0x23, 0x28,

				// random data
				0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8,
			},
			want: &Data{
				remainingData: []byte{0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8},
				AddressFamily: AddressFamilyIPv4,
				Transport:     TransportStream,
				SourceAddr:    []byte{10, 20, 30, 40},
				SourcePort:    8000,
				DestAddr:      []byte{40, 30, 20, 10},
				DestPort:      9000,
			},
		},
		{
			name: "valid tcp4 proxy w TLVs",
			buf: []byte{
				// header
				0x0D, 0x0A, 0x0D, 0x0A, 0x00, 0x0D, 0x0A, 0x51, 0x55, 0x49, 0x54, 0x0A,
				// version/command
				0x21,
				// address family / transport
				0x11,
				// length
				0x0, 0x16,
				// source addr
				10, 20, 30, 40,
				// dest addr
				40, 30, 20, 10,
				// source port
				0x1f, 0x40,
				// dest port
				0x23, 0x28,

				// TLV no-op
				0x4, // No-op
				0x0, 0x0,

				// TLV
				0x3, // CRC32C
				0x0, 0x4,
				1, 2, 3, 4,

				// random data
				0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8,
			},
			want: &Data{
				remainingData: []byte{0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8},
				AddressFamily: AddressFamilyIPv4,
				Transport:     TransportStream,
				SourceAddr:    []byte{10, 20, 30, 40},
				SourcePort:    8000,
				DestAddr:      []byte{40, 30, 20, 10},
				DestPort:      9000,
				TLVs: map[TLVType][]byte{
					TLVTypeNoop:   {},
					TLVTypeCRC32C: {1, 2, 3, 4},
				},
			},
		},
		{
			name: "valid tcp6 proxy",
			buf: []byte{
				// header
				0x0D, 0x0A, 0x0D, 0x0A, 0x00, 0x0D, 0x0A, 0x51, 0x55, 0x49, 0x54, 0x0A,
				// version/command
				0x21,
				// address family / transport
				0x21,
				// length
				0x0, 0x24,
				// source addr
				0x26, 0x7, 0xf8, 0xb0, 0x40, 0x8, 0x8, 0xe, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x20, 0x0e,
				// dest addr
				0x26, 0x6, 0x47, 0x0, 0x47, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x11, 0x11,
				// source port
				0x1f, 0x40,
				// dest port
				0x23, 0x28,

				// random data
				0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8,
			},
			want: &Data{
				remainingData: []byte{0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8},
				AddressFamily: AddressFamilyIPv6,
				Transport:     TransportStream,
				SourceAddr:    []byte{0x26, 0x7, 0xf8, 0xb0, 0x40, 0x8, 0x8, 0xe, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x20, 0x0e},
				SourcePort:    8000,
				DestAddr:      []byte{0x26, 0x6, 0x47, 0x0, 0x47, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x11, 0x11},
				DestPort:      9000,
			},
		},
		{
			name: "valid local",
			buf: []byte{
				// header
				0x0D, 0x0A, 0x0D, 0x0A, 0x00, 0x0D, 0x0A, 0x51, 0x55, 0x49, 0x54, 0x0A,
				// version/command
				0x20,
				// address family / transport
				0x00,
				// length
				0x0, 0x0,

				// random data
				0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8,
			},
			want: &Data{
				remainingData: []byte{0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8},
				AddressFamily: AddressFamilyLocal,
				Transport:     TransportUnspec,
			},
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got, err := Parse(bytes.NewBuffer(tt.buf))

			if tt.wantErr != err {
				t.Fatalf("Parse() err = %v, want %v", err, tt.wantErr)
			}
			if !reflect.DeepEqual(got, tt.want) {
				t.Fatalf("Parse() = %v, want %v", got, tt.want)
			}
		})
	}
}
